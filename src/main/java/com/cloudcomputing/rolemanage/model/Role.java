package com.cloudcomputing.rolemanage.model;

public class Role {
	
	private String role_name;
	private String gae_role;
	private String new_robot;
	private String edit_robot;
	private String view_robot;
	private String edit_user;
	private String edit_role;
	
	public Role(){
		
	}
	public String getRole_name() {
		return role_name;
	}

	public void setRole_name(String role_name) {
		this.role_name = role_name;
	}

	public String getGae_role() {
		return gae_role;
	}

	public void setGae_role(String gae_role) {
		this.gae_role = gae_role;
	}

	public String getNew_robot() {
		return new_robot;
	}

	public void setNew_robot(String new_robot) {
		this.new_robot = new_robot;
	}

	public String getEdit_robot() {
		return edit_robot;
	}

	public void setEdit_robot(String edit_robot) {
		this.edit_robot = edit_robot;
	}

	public String getView_robot() {
		return view_robot;
	}

	public void setView_robot(String view_robot) {
		this.view_robot = view_robot;
	}

	public String getEdit_user() {
		return edit_user;
	}

	public void setEdit_user(String edit_user) {
		this.edit_user = edit_user;
	}

	public String getEdit_role() {
		return edit_role;
	}

	public void setEdit_role(String edit_role) {
		this.edit_role = edit_role;
	}

	
	
	

}
